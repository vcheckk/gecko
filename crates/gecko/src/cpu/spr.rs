#[chapa::bitfield(u32, width = 32, order = msb0)]
#[derive(Clone, Copy, Default)]
pub struct Srr0 {
    #[bits(0..=29)]
    pub value: u32, // low 2 bits are always 0
}

#[chapa::bitfield(u32, width = 32, order = msb0)]
#[derive(Clone, Copy, Default)]
pub struct Xer {
    #[bits(0, alias = "so")]
    pub summary_overflow: bool,
    #[bits(1, alias = "ov")]
    pub overflow: bool,
    #[bits(2, alias = "ca")]
    pub carry: bool,
    #[bits(25..=31)]
    pub byte_count: u8,
}

